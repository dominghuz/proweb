
// Exercício soma dos números de 1 a 10
function soma1a10(){

    // Inicialização da variavel de soma 
    let soma = 0

    // loop para somar os números de 1 a 10
    for (let i = 1; i <=10; i++) {
        soma +=1
        
    }

    // saída do resultado
    console.log("A soma dos números de 1 a 10 é: ", soma)
}

// chamada da função
soma1a10()